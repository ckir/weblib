/**
 * Converts Yahoo Finance API chart data to OHLC format.
 *
 * @param {object} yahooResponse The raw response object from Yahoo Finance API.
 * @returns {Array<object>} An array of OHLC data objects, or an empty array if input is invalid.
 * Each object will have: timestamp (ms), open, high, low, close (unadjusted), adjClose (adjusted), volume.
 */
function yahooToOHLC(yahooResponse) {
  if (
    !yahooResponse ||
    !yahooResponse.chart ||
    !yahooResponse.chart.result ||
    !yahooResponse.chart.result.length ||
    !yahooResponse.chart.result[0]
  ) {
    console.error("Invalid Yahoo Finance response structure: Missing chart.result[0]");
    return [];
  }

  const result = yahooResponse.chart.result[0];

  if (!result.timestamp || !result.indicators) {
    console.error("Invalid Yahoo Finance response structure: Missing timestamp or indicators");
    return [];
  }

  const timestamps = result.timestamp;
  const quotes = result.indicators.quote && result.indicators.quote[0] ? result.indicators.quote[0] : null;
  const adjCloseValues = result.indicators.adjclose && result.indicators.adjclose[0] ? result.indicators.adjclose[0].adjclose : null;

  if (!quotes || !adjCloseValues) {
    console.error("Invalid Yahoo Finance response structure: Missing quote or adjclose data");
    return [];
  }

  if (
    !quotes.open ||
    !quotes.high ||
    !quotes.low ||
    !quotes.close || // Added check for original close
    !quotes.volume ||
    !adjCloseValues
  ) {
    console.error("Invalid Yahoo Finance response structure: Missing OHLVC or adjclose arrays");
    return [];
  }

  const { open, high, low, close, volume } = quotes; // Destructure original close

  const ohlcData = [];

  const numDataPoints = timestamps.length;
  if (
    open.length !== numDataPoints ||
    high.length !== numDataPoints ||
    low.length !== numDataPoints ||
    close.length !== numDataPoints || // Added check for original close length
    volume.length !== numDataPoints ||
    adjCloseValues.length !== numDataPoints
  ) {
    console.error("Mismatched array lengths in indicator data. Processing up to the shortest array.");
    // Potentially, you might want to find the minimum length and iterate up to that.
    // For now, we'll proceed assuming timestamps.length is the reference,
    // but a more robust solution would handle this gracefully.
  }


  for (let i = 0; i < numDataPoints; i++) {
    // Ensure all data points exist for the current index
    if (
      timestamps[i] === undefined ||
      open[i] === undefined ||
      high[i] === undefined ||
      low[i] === undefined ||
      close[i] === undefined || // Added check for original close
      adjCloseValues[i] === undefined ||
      volume[i] === undefined
    ) {
      console.warn(`Skipping data point at index ${i} due to missing data.`);
      continue;
    }

    ohlcData.push({
      timestamp: timestamps[i] * 1000, // Convert seconds to milliseconds
      open: open[i],
      high: high[i],
      low: low[i],
      close: close[i], // Using original close
      adjClose: adjCloseValues[i], // Using adjusted close for 'adjClose'
      volume: volume[i],
    });
  }

  return ohlcData;
}


